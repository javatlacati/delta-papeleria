    private boolean esValidoLaInformacionDeEntrada(String nombre, String precioReferencia, String marca) {
        boolean verificado = false;

        if (!nombre.isEmpty()) {
            if (!marca.isEmpty()) {
                if (!precioReferencia.isEmpty()) {
                    if (esNumeroDecimal(precioReferencia)) {
                        verificado = true;
                    }
                } else {
                    verificado = true;
                }
            } else {
                JOptionPane.showMessageDialog(null, "la Marca no puede ser vacia");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El nombre del producto no puede ser vacio");
        }


        return verificado;
    }

    /**
     * este metodo verifica si un numero es decimal
     *
     * @param numero
     * @return boolean
     */
    private boolean esNumeroDecimal(String numero) {
        boolean verificado = true;
        try {
            Float.parseFloat(numero);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio referencia es invalido");
            verificado = false;
        }
        return verificado;
    }

    /**
     * este metodo verifica si un numero es decimal
     *
     * @param numero
     * @return boolean
     */
    private boolean esNumero(String numero) {
        boolean verificado = true;
        try {
            Integer.valueOf(numero);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio referencia es invalido");
            verificado = false;
        }
        return verificado;
    }

